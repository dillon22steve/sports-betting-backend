package com.sports_betting.app.model.games.results_api;

public class Score {

        private String name;

        private int score;


        public Score() {
        }



        public String getName() {
            return name;
        }

        public void setName(String name) {
            this.name = name;
        }

        public int getScore() {
            return score;
        }

        public void setScore(int score) {
            this.score = score;
        }
    }
